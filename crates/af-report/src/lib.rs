/// Reproducibility metadata for a report. Designed to be **deterministic**
/// given identical inputs and tool versions (NFR-004): no wall-clock timestamp
/// or runtime duration is included. Wall-clock telemetry, if needed, belongs
/// in a separate sidecar artifact.
#[derive(Clone, Debug, Deserialize, JsonSchema, Serialize, PartialEq, Eq)]
pub struct Reproducibility {
    pub host_os: String,
    pub host_arch: String,
    /// FNV1a64 hash of the sorted tool-version list, formatted as a 16-char
    /// hex string. Same env + same `tool_versions` → same hash, so the
    /// report stays bit-deterministic across runs on the same host.
    pub environment_hash: String,
    pub af_version: String,
}

impl Reproducibility {
    pub fn capture(tool_versions: &[ToolVersion]) -> Self {
        Self {
            host_os: std::env::consts::OS.to_string(),
            host_arch: std::env::consts::ARCH.to_string(),
            environment_hash: environment_hash_of(tool_versions),
            af_version: env!("CARGO_PKG_VERSION").to_string(),
        }
    }
}

fn environment_hash_of(tool_versions: &[ToolVersion]) -> String {
    let mut entries: Vec<String> = tool_versions
        .iter()
        .map(|tv| {
            format!(
                "{}={}",
                tv.tool,
                tv.version.as_deref().unwrap_or("unavailable")
            )
        })
        .collect();
    entries.sort();
    let mut hash: u64 = 0xcbf29ce484222325;
    for entry in entries {
        for byte in entry.bytes() {
            hash ^= u64::from(byte);
            hash = hash.wrapping_mul(0x100000001b3);
        }
        hash ^= u64::from(b'\n');
        hash = hash.wrapping_mul(0x100000001b3);
    }
    format!("{hash:016x}")
}

