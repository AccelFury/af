    #[test]
    fn board_evidence_lists_placeholder_boards_explicitly() {
        let manifest = manifest_with_boards(&["sipeed_tang_nano_1k", "tang_nano_20k"]);
        let placeholders = vec!["sipeed_tang_nano_1k".to_string()];
        let inputs = MaturityInputs {
            manifest: Some(&manifest),
            artifacts: &[],
            warnings: &[],
            limitations: &[],
            ci_evidence: &[],
            current_commit_sha: None,
            placeholder_boards: &placeholders,
        };
        let maturity = reusable_core_maturity(&inputs);
        let row = board_row(&maturity);
        assert_eq!(row.status, "planned");
        assert!(row
            .evidence
            .iter()
            .any(|line| line.contains("sipeed_tang_nano_1k") && line.contains("(draft)")));
        assert!(row
            .evidence
            .iter()
            .any(|line| line.contains("tang_nano_20k") && line.contains("(verified-or-unknown)")));
        assert!(
            row.limitations
                .iter()
                .any(|line| line.contains("sipeed_tang_nano_1k")
                    && line.contains("draft_placeholder"))
        );
    }

