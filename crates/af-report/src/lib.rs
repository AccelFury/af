#[derive(Debug, Error)]
pub enum ReportError {
    #[error("failed to serialize report: {message}")]
    Serialize { message: String },
    #[error("failed to create report directory `{path}`: {message}")]
    CreateDir { path: PathBuf, message: String },
    #[error("failed to write report `{path}`: {message}")]
    Write { path: PathBuf, message: String },
}

impl ReportError {
    pub fn code(&self) -> &'static str {
        match self {
            ReportError::Serialize { .. } => "AF_REPORT_SERIALIZE_FAILED",
            ReportError::CreateDir { .. } => "AF_REPORT_CREATE_DIR_FAILED",
            ReportError::Write { .. } => "AF_REPORT_WRITE_FAILED",
        }
    }

    pub fn hint(&self) -> &'static str {
        match self {
            ReportError::Serialize { .. } => "Check report data for unsupported values.",
            ReportError::CreateDir { .. } | ReportError::Write { .. } => {
                "Check filesystem permissions and the selected build root."
            }
        }
    }

    pub fn exit_code(&self) -> i32 {
        5
    }
}

