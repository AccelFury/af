impl AfReport {
    pub fn new(status: impl Into<String>) -> Self {
        Self {
            schema_version: "0.1".to_string(),
            kind: "accelfury.report".to_string(),
            generated_by: GENERATED_BY.to_string(),
            report_version: "0.1".to_string(),
            status: status.into(),
            core: None,
            tool_versions: vec![ToolVersion::available("af", env!("CARGO_PKG_VERSION"))],
            commands: Vec::new(),
            artifacts: Vec::new(),
            warnings: Vec::new(),
            limitations: Vec::new(),
            maturity: None,
            standards: None,
            reproducibility: None,
            command_payload: None,
        }
    }

    /// Populate `reproducibility` from the report's current `tool_versions`.
    /// Idempotent: calling twice with the same `tool_versions` yields the
    /// same hash. Safe to call after `merge_backend`.
    pub fn with_reproducibility(mut self) -> Self {
        self.reproducibility = Some(Reproducibility::capture(&self.tool_versions));
        self
    }

    pub fn for_core(status: impl Into<String>, manifest: &CoreManifest) -> Self {
        let mut report = Self::new(status);
        report.core = Some(CoreIdentity::from(manifest));
        report
            .limitations
            .extend(manifest.known_limitations.clone());
        report
    }

    pub fn merge_backend(&mut self, backend: &BackendReport) {
        self.tool_versions
            .extend(backend.tool_versions.iter().cloned());
        self.commands.extend(backend.commands.iter().cloned());
        self.artifacts.extend(
            backend
                .artifacts
                .iter()
                .map(|path| path.display().to_string()),
        );
        self.warnings.extend(backend.warnings.iter().cloned());
        self.limitations.extend(backend.limitations.iter().cloned());
    }
}

